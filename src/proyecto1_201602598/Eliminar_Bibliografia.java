    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:

        int tipo;
        boolean msj=false;

        int ind=combotipo.getSelectedIndex();

        switch(ind){

            case(0):

            for(int i=0;i<Bibliografia.longibibliografia;i++){

                String a=Bibliografia.bibliografia[i];
                String[] coo=a.split(";");

                if(txtautor.getText().equals(coo[1])){

                    txttitulo.setText(coo[2]);
                    txtdescripcion.setText(coo[3]);
                    txtpalabrasclave.setText(coo[4]);
                    txtedicion.setText(coo[5]);
                    txttemas.setText(coo[6]);
                    txtcopias.setText(coo[10]);
                    txtdisponible.setText(coo[11]);
                    k=i;
                    msj=true;
                    valor=0;

                }

            }
            if(msj==false){
                JOptionPane.showMessageDialog(null, "No se ha encontrado este autor en libros",
                    "Aviso",WARNING_MESSAGE);

            }
            break;

            case(1):

            for(int i=0;i<Revista.longibibliografia;i++){

                String a=Revista.bibliografia[i];
                String[] coo=a.split(";");

                if(txtautor.getText().equals(coo[1])){

                    txttitulo.setText(coo[2]);
                    txtdescripcion.setText(coo[3]);
                    txtpalabrasclave.setText(coo[4]);
                    txtedicion.setText(coo[5]);
                    txttemas.setText(coo[6]);
                    txtfrecuencia.setText(coo[7]);
                    txtejemplares.setText(coo[8]);
                    txtcopias.setText(coo[10]);
                    txtdisponible.setText(coo[11]);
                    k=i;
                    msj=true;
                    valor=1;

                }

            }
            if(msj==false){
                JOptionPane.showMessageDialog(null, "No se ha encontrado este autor en Revistas",
                    "Aviso",WARNING_MESSAGE);

            }
            break;

            case(2):

            for(int i=0;i<Tesis.longibibliografia;i++){

                String a=Tesis.bibliografia[i];
                String[] coo=a.split(";");

                if(txtautor.getText().equals(coo[1])){

                    txttitulo.setText(coo[2]);
                    txtdescripcion.setText(coo[3]);
                    txtpalabrasclave.setText(coo[4]);
                    txtedicion.setText(coo[5]);
                    txttemas.setText(coo[6]);
                    txtarea.setText(coo[9]);
                    txtcopias.setText(coo[10]);
                    txtdisponible.setText(coo[11]);
                    k=i;
                    msj=true;
                    valor=2;

                }

            }
            if(msj==false){
                JOptionPane.showMessageDialog(null, "No se ha encontrado este autor en Revistas",
                    "Aviso",WARNING_MESSAGE);

            }
            break;

        }//switch

    }//GEN-LAST:event_btnbuscarActionPerformed

