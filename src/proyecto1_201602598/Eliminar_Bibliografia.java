    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        // TODO add your handling code here:
        /* Bibliografia b=new Bibliografia();
        Revista r=new Revista();
        Tesis t=new Tesis();*/
        switch(valor){

            case(0):

            Bibliografia.setBibliografia(k);

            JOptionPane.showMessageDialog(null, "Libro eliminado","Aviso",WARNING_MESSAGE);
            Limpiar();
            break;

            case(1):

            Revista.setRevista(k);

            JOptionPane.showMessageDialog(null, "Revista eliminada","Aviso",WARNING_MESSAGE);
            Limpiar();
            break;

            case(2):

            Tesis.setTesis(k);

            JOptionPane.showMessageDialog(null, "Tesis Actualizada","Aviso",WARNING_MESSAGE);
            Limpiar();
            break;

        }

    }//GEN-LAST:event_btnmodificarActionPerformed

